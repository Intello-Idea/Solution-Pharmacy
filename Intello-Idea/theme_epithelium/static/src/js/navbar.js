function show_how_to_use() {

    var a = document.getElementById("nav-home");
    var b = document.getElementById("nav-profile");
    var c = document.getElementById("nav-contact");
    var d = document.getElementById("nav-about");
    var e = document.getElementById("nav-tips");

    if (a.style.display === "none") {
        a.style.display = 'block';
    }

    if (b.style.display === "block") {
        b.style.display = "none";
    } else {
        b.style.display = 'none';
    }

    if (c.style.display === "block") {
        c.style.display = 'none';
    } else {
        c.style.display = 'none';
    }

    if (d.style.display === "block") {
        d.style.display = 'none';
    } else {
        d.style.display = 'none';
    }
    if (e.style.display === "block") {
        e.style.display = 'none';
    } else {
        e.style.display = 'none';
    }

}

